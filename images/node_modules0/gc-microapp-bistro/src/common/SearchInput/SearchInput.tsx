import React from 'react';

import { Icon } from '@buffet/icon';
import { LabeledInput } from '@buffet/input';

import './SearchInput.scss';

interface SearchInputProps {
  label: string;
  value: string;
  disabled?: boolean;
  onChange: (e: React.ChangeEvent<HTMLInputElement>) => void;
}

export function SearchInput({ label, value, disabled, onChange }: SearchInputProps) {
  return (
    <div className={`search-input ${disabled ? 'disabled' : ''}`}>
      <div className="icon">
        <Icon iconId="magnifyingGlass" />
      </div>
      <LabeledInput
        id="search-box"
        label={label}
        type="text"
        value={value}
        onChange={onChange}
        autoFocus={true}
        disabled={disabled}
      />
    </div>
  );
}
