import React from 'react';
import { Link } from 'react-router-dom';

import { RestaurantSearchItemDTO } from '../types';

interface RestaurantResultProps {
  restaurants: RestaurantSearchItemDTO[];
}

export function RestaurantResult({ restaurants }: RestaurantResultProps ) {
  return <ul className="restaurant-result list">
    {restaurants.map(r => (
      <li key={r.rid}>
        <span className="id">{r.rid}</span>
        <span className="name">
          <p>
            <a href={`/restaurant/${r.rid}/home`} target="_blank">{r.name}</a>
          </p>
          <p>
            {r.addressResponse
            ? <span>{r.addressResponse.city} {r.addressResponse.country}</span>
            : (null)
            }
          </p>
        </span>
        <span className="action">
          <Link to={`/restaurants/${r.rid}/diagnostics`}>Diagnostics</Link>
          <Link to={`/restaurants/${r.rid}/permissions`}>Permissions</Link>
          <Link to={`/restaurants/${r.rid}/agreements`}>Agreements</Link>
          <Link to={`/restaurants/${r.rid}/everclear`}>Everclear</Link>
        </span>
      </li>
    ))}
  </ul>;
}
