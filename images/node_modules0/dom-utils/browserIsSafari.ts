let isSafariBrowser: boolean | null = null;

export const isSafari = (): boolean => {
  if (isSafariBrowser === null) {
    if (typeof window !== 'undefined' && window.navigator.userAgent) {
      const chrome = window.navigator.userAgent.indexOf('Chrome') > -1;
      const safari = window.navigator.userAgent.indexOf('Safari') > -1;
      isSafariBrowser = safari && !chrome;
    }
  }

  return isSafariBrowser || false;
};
