let isIEBrowser: boolean | null = null;

export const isIE = (): boolean => {
  if (isIEBrowser === null) {
    if (typeof window !== 'undefined' && window.navigator.userAgent) {
      const ua = window.navigator.userAgent;
      isIEBrowser = (ua.indexOf('MSIE ') > 0 || ua.indexOf('Trident/') > 0);
    }
  }

  return isIEBrowser || false;
};
