import React from 'react';
import classnames from 'classnames';
import PropTypes from 'prop-types';
import './Nav.scss';

/**
 * The `Nav` component renders a collapsable side bar inside a `Layout` container.
 * It can be used along NavLink (from react-router) and NavIcon and NavLabel to
 * create a consistent collapsable sidebar navigation UI in a MicroApp.
 */
export function Nav(props) {
  return (
    <div className={classnames('gca-nav', { collapsed: props.collapsed })}>
      {React.Children.toArray(props.children)}
    </div>
  );
}

Nav.propTypes = {
  children: PropTypes.node,
  collapsed: PropTypes.bool,
};

Nav.defaultProps = {
  collapsed: false,
};

export default Nav;
