import { shallow } from 'enzyme';
import React from 'react';

import { Layout } from './Layout';

describe('<Layout />', () => {
  it('default snapshot', () => {
    const wrapper = shallow(
      <Layout title="Title">
        <p>Test</p>
      </Layout>,
    );

    expect(wrapper).toMatchSnapshot();
  });

  it('title bar content snapshot', () => {
    const wrapper = shallow(
      <Layout title="Title" titleBarContent={<a href="http://blank.org">view live profile</a>}>
        <p>Test</p>
      </Layout>,
    );

    expect(wrapper).toMatchSnapshot();
  });
});
