import { shallow } from 'enzyme';
import * as React from 'react';

import { SwitchListFinalField } from './SwitchListFinalField';

describe('<SwitchListFinalField />', () => {

  it('should render the basic field', () => {
    expect(
      shallow(
        <SwitchListFinalField
          name="mytext"
          options={[
            { label: 'First item', id: 'first' },
            { label: 'Second item', id: 'second' }]
          }
        />)
      ).toMatchSnapshot();
  });

  it('should render the field as invalid', () => {
    expect(
      shallow(
        <SwitchListFinalField
          name="mytext"
          options={[
            { label: 'First item', id: 'first' },
            { label: 'Second item', id: 'second' }]}
          valid={false}
        />)
      ).toMatchSnapshot();
  });

});
