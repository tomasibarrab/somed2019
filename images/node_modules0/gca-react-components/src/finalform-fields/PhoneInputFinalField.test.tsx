import { shallow } from 'enzyme';
import * as React from 'react';

import { PhoneInputFinalField } from './PhoneInputFinalField';

describe('<PhoneInputFinalField />', () => {

  it('should render the basic field', () => {
    expect(
      shallow(
        <PhoneInputFinalField
          name="mytext"
        />)
      ).toMatchSnapshot();
  });

  it('should render the phone field with international format', () => {
    expect(
      shallow(
        <PhoneInputFinalField
          name="mytext"
          phoneFormat="International"
        />)
      ).toMatchSnapshot();
  });

  it('should render the field as invalid', () => {
    expect(
      shallow(
        <PhoneInputFinalField
          name="mytext"
          valid={false}
        />)
      ).toMatchSnapshot();
  });

});
