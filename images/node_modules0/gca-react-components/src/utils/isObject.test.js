import isObject from './isObject';

describe('isObject', () => {
  it('Object', () => {
    expect(isObject({})).toBeTruthy();
  });

  it('Null', () => {
    expect(isObject(null)).toBeFalsy();
  });

  it('Array', () => {
    expect(isObject([])).toBeFalsy();
  });

  it('Number', () => {
    expect(isObject(1)).toBeFalsy();
  });

  it('String', () => {
    expect(isObject('foo')).toBeFalsy();
  });

  it('Function', () => {
    expect(isObject(() => {})).toBeFalsy();
  });

  it('Undefined', () => {
    expect(isObject()).toBeFalsy();
  });

  it('NaN', () => {
    expect(isObject(NaN)).toBeFalsy();
  });
});
