import React from 'react';

interface ItemCellProps {
  title?: string;
  children?: React.ReactNode;
  testid?: string;
}

export function ItemCell(p: ItemCellProps) {
  return (
    <td data-testid={p.testid} className="table-cell" title={p.title || ''}>{p.children}</td>
  );
}

export function HeaderCell(p: ItemCellProps) {
  return (
    <th className="table-header-cell">
      <div className="flex-cell" title={p.title || ''}>
        {p.children}
      </div>
    </th>
  )
}
