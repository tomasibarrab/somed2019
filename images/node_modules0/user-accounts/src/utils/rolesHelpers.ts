export const RIDROLE_ALL_ACCESS = 'urn:ot.rest.roles:aaallaccess';
export const GROUPROLE_ALL_ACCESS = 'urn:ot.group.roles:allaccess';
export const GROUPROLE_REST_ALL_ACCESS = 'urn:ot.groups.roles:aaallaccess';


const isAllAccessGroup = (role: string): boolean => {
  return role === GROUPROLE_ALL_ACCESS;
};

const isAllAccessRestaurant = (role: string): boolean => {
  return role === RIDROLE_ALL_ACCESS || role === GROUPROLE_REST_ALL_ACCESS;
};

export const isRoleAllAccess = (role: string ): boolean => {
  return isAllAccessGroup(role) || isAllAccessRestaurant(role);
};

export const isRoleGroupAllAccess = <T extends { urn: string }>(role: T): boolean => {
  return isAllAccessGroup(role.urn);
};

export const isRoleRestaurantAllAccess = <T extends { urn: string }>(role: T): boolean => {
  return isAllAccessRestaurant(role.urn);
};
