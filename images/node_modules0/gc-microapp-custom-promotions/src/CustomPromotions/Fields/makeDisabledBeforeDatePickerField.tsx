import { formValues } from 'redux-form';
import DatePickerField from './DatePickerField';

/* Putting the formValues here allows only re-rendering the DatePickerField that
  needs to be adjusted, rather than the entirety of the component that contains it */
const makeDisabledBeforeDatePickerField = (fieldToDisableBefore: string) => {
  return formValues<any, any>({ disableBefore: fieldToDisableBefore })(DatePickerField);
};

export default makeDisabledBeforeDatePickerField;
