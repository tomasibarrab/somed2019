import React, { SFC } from 'react';
import DayCheckmark from './DayCheckmark';

interface Props {
  selected: boolean;
  label: string;
  onClickDay(value: any): void;
}

const ImageDay: SFC<Props> = ({ selected, label, onClickDay }) => (
  <div className={`day-label-container ${selected ? 'selected-day' : ''}`} onClick={() => onClickDay(true)}>
    <span className="day-label-text">
      {label.substr(0, 3)} <DayCheckmark day={label} />
    </span>
  </div>
);

export default ImageDay;
