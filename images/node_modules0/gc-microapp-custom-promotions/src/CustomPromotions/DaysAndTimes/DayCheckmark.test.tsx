import { mount } from 'enzyme';
import React, { SFC } from 'react';
import { DayCheckmark } from './DayCheckmark';

describe('<DayCheckmark />', () => {
  test('it matches snapshot', () => {
    const props = { day: 'Sunday' };
    const wrapper = mount(<DayCheckmark {...props} />);
    expect(wrapper.debug()).toMatchSnapshot();
  });

  test('it matches snapshot when day is selected', () => {
    const props = { day: 'Sunday', sunday: { morning: true } };
    const wrapper = mount(<DayCheckmark {...props} />);
    expect(wrapper.debug()).toMatchSnapshot();
  });
});
