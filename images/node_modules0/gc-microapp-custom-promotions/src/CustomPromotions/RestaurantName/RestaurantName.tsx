import { selectors } from 'gca-react-components/src/redux-modules/currentContext';
import React, { Component } from 'react';
import { connect } from 'react-redux';
import { State } from '../../store/types';

/*
  This will eventually have
  more in its state
  to handle selecting rid
*/

import './RestaurantName.scss';

interface SP {
  restaurantName: string;
}

type Props = SP;

class RestaurantName extends Component<Props, {}> {
  constructor(props: Props) {
    super(props);
  }

  public render() {
    const { restaurantName } = this.props;
    return (
      <header className="restaurant-name">
        <h3>{restaurantName}</h3>
      </header>
    );
  }
}

const mapState = (state: State): SP => ({
  restaurantName: selectors.restaurant(state).name,
});

export default connect<SP, {}, {}>(mapState, {})(RestaurantName);
