import classnames from 'classnames';
import React, { Component, ReactFragment } from 'react';

/* Stolen from gca-react-components */
import './Modal.scss';

interface Props {
  children: ReactFragment;
  modalClass?: string;
  onClose: () => void;
  overlayClass?: string;
}

class Modal extends Component<Props> {
  constructor(props: Props) {
    super(props);

    this.handleClose = this.handleClose.bind(this);
  }

  public render() {
    const { overlayClass, modalClass, children } = this.props;

    /* eslint-disable jsx-a11y/no-static-element-interactions */
    return (
      <div onClick={this.handleClose} className={classnames('pi-modal-overlay', overlayClass)}>
        <div className={classnames('pi-modal', modalClass)}>{children}</div>
      </div>
    );
  }

  private handleClose(e: any) {
    if (e.target === e.currentTarget) {
      this.props.onClose();
    }
  }
}

export default Modal;
