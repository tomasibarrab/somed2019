import React from 'react';

const ClipPath = (props: any) => {
  const {
    innerBounds,
    outerBounds,
    padding,
    slideProps,
  } = props;

  if (slideProps && !slideProps.isDisabled) {
    return (
      <defs>
        <clipPath id="mask">
          <rect
            x={(outerBounds.left)}
            y={innerBounds.top}
            width={outerBounds.width}
            height={innerBounds.height + padding.bottom}
            style={{
              fill: '#ffffff',
            }}
          />
        </clipPath>
      </defs>
    )
  }

  return null;
}

export { ClipPath };
