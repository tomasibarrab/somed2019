import { shallow } from 'enzyme';
import React from 'react';

import ReviewSummary from './ReviewSummary';

describe('<ReviewSummary />', () => {
  let props;
  beforeEach(() => {
    props = {
      ratings: {
        Overall: 5,
      },
      rejected: false,
    };
  });

  it('ReviewSummary default snapshot', () => {
    const wrapper = shallow(<ReviewSummary {...props} />);

    expect(wrapper).toMatchSnapshot();
  });

  it('ReviewSummary rejected snapshot', () => {
    props.rejected = true;
    const wrapper = shallow(<ReviewSummary {...props} />);

    expect(wrapper).toMatchSnapshot();
  });
});
