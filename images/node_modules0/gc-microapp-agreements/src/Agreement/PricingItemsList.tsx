import { Pricing } from '@buffet/user-agreement';
import { LangLocale } from 'gca-react-components/src/redux-modules/languageProvider';
import React from 'react';

import PricingItem from './PricingItem';


interface PricingListProps {
  items: Pricing[];
  locale: LangLocale;
}

const PricingItemsList = ({ items, locale }: PricingListProps) => {
  return (
    <ul>
      {items
        .filter(item => item.listPrice !== 0)
        .map(item => (
          <li>
            <PricingItem item={item} locale={locale} />
          </li>
        ))
      }
    </ul>
  );
};

export default PricingItemsList;
