import React, { ReactNode } from 'react';
import { FormattedMessage } from 'react-intl';
import './SimpleCard.scss';

interface Props {
  title: string;
  description: string;
  children?: ReactNode;
}

export default function SimpleCard({ title, description, children }: Props) {
  return (
    <div className="simplecard">
      {children}
      <FormattedMessage tagName="h2" id={title} />
      <FormattedMessage tagName="p" id={description} />
    </div>
  );
}
