import { ValidationErrorMessages } from '../constants';
const validate = (values: any) => {
  const { name, restaurantLegalName, agreed } = values;
  const errors: any = {};
  if (!name) {
    errors.name = ValidationErrorMessages.Required;
  }
  if (!restaurantLegalName) {
    errors.restaurantLegalName = ValidationErrorMessages.Required;
  }
  if (!agreed) {
    errors.agreed = ValidationErrorMessages.Required;
  }
  return errors;
};

export default validate;
