import { ValidationErrorMessages } from './constants';

const urlRegex = /^(http|https):\/\/[^ "]+$/;

const validateUrl = (values: any) => {
  const { url } = values;
  const errors: any = {};
  if (!url) {
    errors.url = ValidationErrorMessages.Required;
  }
  if (!urlRegex.test(url)) {
    errors.url = ValidationErrorMessages.ValidUrl;
  }
  return errors;
};

export default validateUrl;
