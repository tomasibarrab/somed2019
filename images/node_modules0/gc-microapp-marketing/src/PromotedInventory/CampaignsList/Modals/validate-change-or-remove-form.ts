import { DiscardActionTypes } from '../../../store/campaigns/campaigns.types';
import { FormValues } from './ChangeOrRemoveModal';

const validate = ({ action, description }: FormValues) => {
  const errors: any = {};
  if (action === DiscardActionTypes.RequestChange && !description) {
    errors.description = true;
  }
  return errors;
};

export default validate;
