import React from 'react';

import classNames from 'classnames';

import './SpeechDialog.scss';

const SpeechDialog = ({ children, direction, header, lg, sm }) => {
  const className = classNames(
    'speechdialog',
    `speechdialog--direction-${direction}`,
    { 'speechdialog--sm': sm },
    { 'speechdialog--lg': lg },
  );

  return (
    <div className={className}>
      <div className="speechdialog__container">
        {header}
        <div className="speechdialog__content">{children}</div>
      </div>
    </div>
  );
};

SpeechDialog.defaultProps = {
  children: undefined,
  direction: 'top',
  header: null,
  lg: false,
  sm: false,
};

export default SpeechDialog;
