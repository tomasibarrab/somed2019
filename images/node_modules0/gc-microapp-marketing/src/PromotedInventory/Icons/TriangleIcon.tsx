import React, { SFC } from 'react';
import dimensionWrapper from '../Insights/helpers/dimensionWrapper';

// tslint:disable:object-literal-sort-keys
const mapDirection = {
  up: 180,
  down: 0,
  left: 270,
  right: 90,
};

export const TriangleIcon: SFC = dimensionWrapper(({ width, height, color, className, strokeWidth, direction }) => (
  <svg width={width} height={height} className={className} viewBox="0 0 6 8" xmlns="http://www.w3.org/2000/svg">
    <g fill="none" strokeWidth={strokeWidth}>
      <polygon
        transform={`translate(3 4)scale(1 -1)translate(-3 -4)rotate(${mapDirection[direction]} 3 4)`}
        points="3 0 6 8 0 8"
        fill={color}
      />
    </g>
  </svg>
));

TriangleIcon.defaultProps = {
  height: 14,
  color: '#929495',
  strokeWidth: 1,
  direction: 'down',
};
