import React, { SFC } from 'react';
import dimensionWrapper from '../Insights/helpers/dimensionWrapper';

export const CircleIcon: SFC = dimensionWrapper(({ radius, color, className }) => (
  <svg
    width={radius}
    height={radius}
    className={className}
    viewBox={`0 0 ${radius} ${radius}`}
    xmlns="http://www.w3.org/2000/svg"
  >
    <g fill="none" fillRule="evenodd">
      <circle fill={color} cx={radius / 2} cy={radius / 2} r={radius / 2} />
    </g>
  </svg>
));

CircleIcon.defaultProps = {
  color: '#929495',
  radius: 24,
};
