import { Panel } from 'gca-react-components';
import * as React from 'react';
import { FormattedMessage, InjectedIntlProps, injectIntl } from 'react-intl';
import { LeadGenIcon } from '../Lead/LeadGenIcon';
import { PrivateDiningIcon } from '../PrivateDiningIcon';

function getEmailAddr(props: InjectedIntlProps): string {
  return props.intl.formatMessage({ id: 'privatedining.enabled.body.email' });
}

function getAnchorHref(props: InjectedIntlProps): string {
  return (
    'mailto:' +
    getEmailAddr(props) +
    '?subject=' +
    props.intl.formatMessage({ id: 'privatedining.title' }) +
    '&body=' +
    '%0A' +
    props.intl.formatMessage({ id: 'privatedining.enabled.body.email.message' }) +
    '%0A%0A' +
    props.intl.formatMessage({ id: 'privatedining.enabled.body.email.thankyou' })
  );
}

function PrivateDiningEnabled(props: InjectedIntlProps) {
  return (
    <Panel title={props.intl.formatMessage({ id: 'privatedining.title' })}>
      <section className="gca-private-dining">
        <div className="content-container">
          <PrivateDiningIcon />
          <div className="title-text">
            <FormattedMessage id="privatedining.enabled.heading" />
          </div>
          <div className="enabled-body-text">
            <FormattedMessage id="privatedining.enabled.body" />
            <div>
              <span className="sentence-spacing">
                <a href={getAnchorHref(props)}>{getEmailAddr(props)}</a>
              </span>
              <span className="sentence-spacing">
                <FormattedMessage id="privatedining.enabled.body.phone" />
              </span>
            </div>
          </div>
        </div>
      </section>
    </Panel>
  );
}

export default injectIntl<{}>(PrivateDiningEnabled);
