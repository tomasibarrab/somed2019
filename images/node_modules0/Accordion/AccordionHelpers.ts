import { AccordionProps } from './AccordionTypes';


export function getClassNames({ isOpen, animateScale, animateOpacity }: Partial<AccordionProps>) {
  const classNames = [];

  if (isOpen) { classNames.push('isOpen'); }
  if (animateScale) { classNames.push('withScale'); }
  if (animateOpacity) { classNames.push('withOpacity'); }

  return classNames.join(' ');
}
