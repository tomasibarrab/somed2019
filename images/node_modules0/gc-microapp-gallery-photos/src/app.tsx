import { IntlProvider, registerMessages } from '@buffet/intl';
import React from 'react';
import ReactDOM from 'react-dom';
import { getGcScope } from 'shared-utils/gcScope';
import App from './common/App/App';
import { translationMessages } from './translations/i18n';

registerMessages(translationMessages);

const gcScope = getGcScope(window);
const rid = gcScope.id ? parseInt(gcScope.id.toString(), 10) : 0;

const render = () => {
  ReactDOM.render(
    <IntlProvider>
      <App rid={rid} />
    </IntlProvider>,
    document.querySelector('#app'),
  );
};

render();
